function getTranslation(key, language) {
  if (language === "hi" && hindiDictionary[key]) {
    return hindiDictionary[key];
  }

  return defaults.text.get(key) || "";
}

function getPlaceholderTranslation(key, language) {
  if (language === "hi" && hindiDictionary[key]) {
    return hindiDictionary[key];
  }

  return defaults.placeholder.get(key) || "";
}

function syncThemeLabel() {
  if (!themeToggleLabel) {
    return;
  }

  const labelGroup = themeLabels[currentLanguage] || themeLabels.en;
  themeToggleLabel.textContent = body.dataset.theme === "dark" ? labelGroup.dark : labelGroup.light;
}

function setTheme(theme) {
  body.dataset.theme = theme;
  localStorage.setItem("agritech-theme", theme);
  syncThemeLabel();
}

function getInitialTheme() {
  if (storedTheme) {
    return storedTheme;
  }

  return window.matchMedia("(prefers-color-scheme: light)").matches ? "light" : "dark";
}

function applyLanguage(language) {
  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = getTranslation(key, language);
  });

  placeholderNodes.forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    node.placeholder = getPlaceholderTranslation(key, language);
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });

  root.lang = language;
  syncThemeLabel();
}

function setLanguage(language) {
  currentLanguage = language;
  localStorage.setItem("agritech-language", language);
  applyLanguage(language);
}

function syncActiveNavigation() {
  const currentFile = window.location.pathname.split("/").pop() || "index.html";

  navLinks.forEach((link) => {
    const href = (link.getAttribute("href") || "").replace("./", "");
    link.classList.toggle("is-active", href === currentFile);
  });
}

function setupHeroMotion() {
  if (!heroSection || !heroScene || prefersReducedMotion.matches) {
    return;
  }

  const motion = {
    currentX: 12,
    currentY: -10,
    targetX: 12,
    targetY: -10,
    hovering: false
  };

  const syncScroll = () => {
    const rect = heroSection.getBoundingClientRect();
    const progress = Math.max(0, Math.min(1, -rect.top / Math.max(rect.height * 0.82, 1)));
    const depth = Math.min(1, Math.pow(progress, 0.9));

    heroSection.style.setProperty("--hero-copy-shift", `${depth * -66}px`);
    heroSection.style.setProperty("--hero-copy-opacity", `${1 - depth * 0.24}`);
    heroSection.style.setProperty("--hero-copy-tilt", `${depth * 4}deg`);
    heroSection.style.setProperty("--hero-scene-y", `${depth * 78}px`);
    heroSection.style.setProperty("--hero-scene-z", `${depth * -160}px`);
    heroSection.style.setProperty("--hero-scene-scale", `${1 - depth * 0.08}`);
    heroSection.style.setProperty("--hero-metric-y", `${depth * -28}px`);
    heroSection.style.setProperty("--hero-metric-opacity", `${1 - depth * 0.28}`);
    heroSection.style.setProperty("--hero-metric-tilt", `${depth * 5}deg`);
    heroSection.style.setProperty("--hero-glow-opacity", `${1 - depth * 0.42}`);

    heroSection.style.setProperty("--hero-grid-y", `${depth * 58}px`);
    heroSection.style.setProperty("--hero-grid-z", `${depth * -190}px`);
    heroSection.style.setProperty("--hero-grid-scale", `${depth * 0.14}`);
    heroSection.style.setProperty("--hero-grid-fade", `${depth * 0.3}`);

    heroSection.style.setProperty("--hero-terrain-y", `${depth * 40}px`);
    heroSection.style.setProperty("--hero-terrain-z", `${depth * 118}px`);
    heroSection.style.setProperty("--hero-terrain-rotate", `${depth * -4}deg`);

    heroSection.style.setProperty("--hero-core-y", `${depth * -34}px`);
    heroSection.style.setProperty("--hero-core-z", `${depth * 86}px`);
    heroSection.style.setProperty("--hero-core-scale", `${depth * 0.08}`);

    heroSection.style.setProperty("--hero-orbit-y", `${depth * -18}px`);
    heroSection.style.setProperty("--hero-orbit-z", `${depth * 110}px`);

    heroSection.style.setProperty("--hero-card-top-x", `${depth * -34}px`);
    heroSection.style.setProperty("--hero-card-top-y", `${depth * -44}px`);
    heroSection.style.setProperty("--hero-card-top-z", `${depth * 80}px`);
    heroSection.style.setProperty("--hero-card-right-x", `${depth * 44}px`);
    heroSection.style.setProperty("--hero-card-right-y", `${depth * 12}px`);
    heroSection.style.setProperty("--hero-card-right-z", `${depth * 60}px`);
    heroSection.style.setProperty("--hero-card-bottom-x", `${depth * -18}px`);
    heroSection.style.setProperty("--hero-card-bottom-y", `${depth * 34}px`);
    heroSection.style.setProperty("--hero-card-bottom-z", `${depth * 46}px`);

    heroSection.style.setProperty("--hero-pill-left-y", `${depth * -18}px`);
    heroSection.style.setProperty("--hero-pill-left-z", `${depth * 28}px`);
    heroSection.style.setProperty("--hero-pill-right-y", `${depth * 24}px`);
    heroSection.style.setProperty("--hero-pill-right-z", `${depth * 34}px`);

    return depth;
  };

  const animate = () => {
    const time = performance.now();
    const depth = syncScroll();

    if (!motion.hovering) {
      motion.targetX = 12 + Math.sin(time / 1800) * 3;
      motion.targetY = -10 + Math.cos(time / 2200) * 2;
    }

    motion.currentX += (motion.targetX - motion.currentX) * 0.08;
    motion.currentY += (motion.targetY - motion.currentY) * 0.08;

    heroScene.style.setProperty("--scene-rotate-x", `${motion.currentY + depth * 8}deg`);
    heroScene.style.setProperty("--scene-rotate-y", `${motion.currentX - depth * 6}deg`);

    requestAnimationFrame(animate);
  };

  heroScene.addEventListener("pointerenter", () => {
    motion.hovering = true;
  });

  heroScene.addEventListener("pointermove", (event) => {
    const bounds = heroScene.getBoundingClientRect();
    const x = (event.clientX - bounds.left) / bounds.width - 0.5;
    const y = (event.clientY - bounds.top) / bounds.height - 0.5;

    motion.targetX = x * 22;
    motion.targetY = y * -16;
  });

  heroScene.addEventListener("pointerleave", () => {
    motion.hovering = false;
  });

  window.addEventListener("scroll", syncScroll, { passive: true });
  window.addEventListener("resize", syncScroll);

  syncScroll();
  requestAnimationFrame(animate);
}

function setupScrollDepthSections() {
  if (prefersReducedMotion.matches || !("IntersectionObserver" in window)) {
    return;
  }

  const sections = document.querySelectorAll(
    ".section-shell:not(.hero), .page-hero, .auth-layout, .project-hero, .project-dashboard, .project-support-grid, .page-section"
  );

  if (!sections.length) {
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -8% 0px"
    }
  );

  sections.forEach((section) => {
    section.classList.add("scroll-depth");

    if (section.getBoundingClientRect().top < window.innerHeight * 0.88) {
      section.classList.add("is-visible");
    }

    observer.observe(section);
  });
}

function setupDemoForms() {
  document.querySelectorAll("[data-demo-form]").forEach((form) => {
    form.addEventListener("submit", (event) => {
      event.preventDefault();

      const resultId = form.dataset.demoForm;
      const resultKey = form.dataset.resultKey;
      const resultNode = document.querySelector(`[data-demo-result="${resultId}"]`);

      if (!resultNode || !resultKey) {
        return;
      }

      resultNode.textContent = demoMessages[currentLanguage]?.[resultKey] || demoMessages.en[resultKey] || "";

      resultNode.hidden = false;
    });
  });
}

